package sep16th;

public class RealtionalOperatorsDemo1 {

	public static void main(String[] args) {
		
		int x=20;
		
		int y=30;
		
		System.out.println(x>y);   // false
		System.out.println(x<y);  //true
		System.out.println(x>=y);  //false
		System.out.println(x<=y);  //true
		System.out.println(x==y);  //false
		System.out.println(x!=y);   //true

	}

}
